const STORAGE_KEY = "db-cache";
const DB_BASE_URL = "../data/db.json"; // üß± ‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏´‡∏•‡∏±‡∏Å (‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô production DB)

